<?php
$i = 0;
echo $i;   // Output: 0

// pre-incrementoperator: verhoogt de waarde voor output 
echo ++$i; // Output: 1
echo $i;   // Output: 1

// post-incrementoperator: verhoogt de waarde na output 
echo $i++; // Output: 1
echo $i;   // Output: 2

// pre-decrementoperator: vermindert de waarde voor output 
echo --$i; // Output: 1
echo $i;   // Output: 1

// post-decrementoperator: vermindert de waarde na output 
echo $i--; // Output: 1
echo $i;   // Output: 0